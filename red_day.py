# Get terminal width; default to 80 columns if not available
term_width = shutil.get_terminal_size((80, 20)).columns

def center_text(text):
    """Center every line in a given multi-line string to term_width."""
    lines = text.splitlines()
    centered = "\n".join(line.center(term_width) for line in lines)
    return centered

# Create an Event to signal when data fetching is done
done_event = threading.Event()

def progress_printer():
    """Print random progress messages every 2 seconds until data is fetched."""
    while not done_event.is_set():
        message = random.choice(progress_messages)
        print(message)
        time.sleep(0.69)

# Start the progress thread
progress_thread = threading.Thread(target=progress_printer)
progress_thread.start()

# Fetch today's intraday data for SPY (1-day, 1-minute intervals)
try:
    with requests.Session() as s:
        s.request = lambda *args, **kwargs: requests.request(*args, timeout=10, **kwargs)  # Set timeout
        data = yf.Ticker("SPY").history(period="1d", interval="1m")
except requests.exceptions.Timeout:
    print("Error: Request timed out after 10 seconds.")
    done_event.set()
    progress_thread.join()
    exit(1)
except Exception as e:
    print("Error fetching data:", e)
    done_event.set()
    progress_thread.join()
    exit(1)


# Signal that fetching is done and wait for the progress thread to finish
done_event.set()
progress_thread.join()

