# List of funny progress messages
progress_messages = [
    "Hiii, lemme drink some coffee and I will give you the data...",
    "Okay, here we go...",
    "Wait for it, here comes the result...",
    "Damn, this is a lotta data to analyze, gimme a break...",
    "...Calculating really complex stuff...",
    "Oh, dear, here we go again..."
]

# Create an Event to signal when data fetching is done
done_event = threading.Event()

def progress_printer():
    """Print random progress messages every 2 seconds until data is fetched."""
    while not done_event.is_set():
        message = random.choice(progress_messages)
        print(message)
        time.sleep(0.69)

# Start the progress thread
progress_thread = threading.Thread(target=progress_printer)
progress_thread.start()

# Fetch today's intraday data for SPY (1-day, 1-minute intervals)
try:
    data = yf.Ticker("SPY").history(period="1d", interval="1m")
except Exception as e:
    done_event.set()
    progress_thread.join()
    print("Error fetching data:", e)
    exit(1)

# Signal that fetching is done and wait for the progress thread to finish
done_event.set()
progress_thread.join()

# Get today's opening price and the current/latest price
